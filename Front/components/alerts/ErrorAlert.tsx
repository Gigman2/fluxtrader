import React from "react";

interface ErrorAlertProps {
  message: string;
  className?: string;
}

const ErrorAlert: React.FC<ErrorAlertProps> = ({ message, className = "" }) => {
  if (!message) return null;

  return (
    <div
      className={`p-3 bg-rose-50 dark:bg-rose-900/20 border border-rose-200 dark:border-rose-800 rounded-xl text-sm text-rose-600 dark:text-rose-400 ${className}`}
    >
      {message}
    </div>
  );
};

export default ErrorAlert;
